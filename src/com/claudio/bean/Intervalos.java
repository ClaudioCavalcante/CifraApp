package com.claudio.bean;

public class Intervalos {

	private int interID;
	private String intervalo_name;
	private String intervalo_char;
	
	
	public int getInterID() {
		return interID;
	}
	public void setInterID(int interID) {
		this.interID = interID;
	}
	public String getIntervalo_name() {
		return intervalo_name;
	}
	public void setIntervalo_name(String intervalo_name) {
		this.intervalo_name = intervalo_name;
	}
	public String getIntervalo_char() {
		return intervalo_char;
	}
	public void setIntervalo_char(String intervalo_char) {
		this.intervalo_char = intervalo_char;
	}
	
	
	
	
}//fim da classe Intervalos
