package com.claudio.bean;

public class Acidentes {

	private int acidentesID;
	private String acidentes_name;
	private String acidentes_char;
	
	
	public int getAcidentesID() {
		return acidentesID;
	}
	public void setAcidentesID(int acidentesID) {
		this.acidentesID = acidentesID;
	}
	public String getAcidentes_name() {
		return acidentes_name;
	}
	public void setAcidentes_name(String acidentes_name) {
		this.acidentes_name = acidentes_name;
	}
	public String getAcidentes_char() {
		return acidentes_char;
	}
	public void setAcidentes_char(String acidentes_char) {
		this.acidentes_char = acidentes_char;
	}
	
	
	
}//fim da classe Acidentes
